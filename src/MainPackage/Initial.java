    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int a = Integer.parseInt(jTextField1.getText());
        
        if(Radio_Fibo.isSelected())
        {
            FiboDivConq fdc = new FiboDivConq();
            FiboProblem fp = new FiboProblem();
            fp.setProblem(a);
            FiboSolution fs = new FiboSolution();
            fs = ((FiboSolution)fdc.solve(fp));
            
            jLabel2.setText("Fibonacci");
            jLabel4.setText(jTextField1.getText());
            jLabel6.setText(""+fs.getSolution());
                    
        }
        if(Radio_Fact.isSelected())
        {
            FactDivConq fdc = new FactDivConq();
            FactProblem fp = new FactProblem();
            fp.setProblem(a);
            FactSolution fs = new FactSolution();
            fs = ((FactSolution)fdc.solve(fp));
            
            jLabel2.setText("Factorial");
            jLabel4.setText(jTextField1.getText());
            jLabel6.setText(""+fs.getSolution());
        }
    }//GEN-LAST:event_jButton1ActionPerformed
